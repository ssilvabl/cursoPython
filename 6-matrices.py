# MATRICES
# Las matrices se pueden crear utilizando listas dentro de una lista
# Una matriz es multidimencional, y un vecor es unidimencional

# Filas
fila_a = [12, 33]
fila_b = [34, 88]

# Crear arreglo o matriz 2 x 2
matriz_c = [fila_a, fila_b]
print(matriz_c)

# Acceder a un elemento de la matriz
# Par√°metros[fila][columna]
print(matriz_c[1][1])

